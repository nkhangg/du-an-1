    public void addData(Box box) {
        BoxItem boxItem = new BoxItem();
        boxItem.setData(box);
        boxItem.addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent me) {
                if (SwingUtilities.isLeftMouseButton(me)) {
                    event.itemClick(boxItem, box);
                }
                
                //goi frame dat box --> truyen vao cai box dc click
                new DatBoxView(box).setVisible(true);
            }
        });

        panelItem.add(boxItem);
        panelItem.repaint();
        panelItem.revalidate();
        panelBill.setPanelItem(panelItem);
    }

